/**
 * Represents a visibility of the ORM-related property
 *
 * @ingroup Orm_Model
 */
final class OrmPropertyVisibility extends Enumeration
{
	/**
	 * Getter + setter
	 */
	const FULL = 'full';

	/**
	 * Only getter (for db columns generated by triggers, and so on)
	 */
	const READONLY = 'readonly';

	/**
	 * Nothing (for db columns that used for inner calculations via triggers in denormalized
	 * database schemas)
	 */
	const TRANSPARENT = 'transparent';

	static private $gettable = array(
		self::FULL, self::READONLY
	);

	static private $settable = array(
		self::FULL
	);

	/**
	 * Getter + setter
	 * @return OrmPropertyVisibility
	 */
	static function full()
	{
		return new self (self::FULL);
	}

	/**
	 *  Only getter (for db columns generated by triggers, and so on)
	 * @return OrmPropertyVisibility
	 */
	static function readonly()
	{
		return new self (self::READONLY);
	}

	/**
	 * Nothing (for db columns that used for inner calculations via triggers in denormalized
	 * @return OrmPropertyVisibility
	 */
	static function transparent()
	{
		return new self (self::TRANSPARENT);
	}

	/**
	 * Determines whether the visibilty allows getting of the property
	 * @return boolean
	 */
	function isGettable()
	{
		return in_array($this->getValue(), self::$gettable);
	}

	/**
	 * Determines whether the visibilty allows setting of the property
	 * @return boolean
	 */
	function isSettable()
	{
		return in_array($this->getValue(), self::$settable);
	}
}

